package com.example.androidcalculator;

public class Calculator {
	double outcome_1;
	float outcome_2;
	
	public double add(double input_1, double input_2){
		outcome_1 = input_1 + input_2;
		return outcome_1;
	}
	
	public double minus(double input_1, double input_2){
		outcome_1 = input_1 + input_2;
		return outcome_1;
	}
	
	public double mutiple(double input_1, double input_2){
		outcome_1 = input_1 + input_2;
		return outcome_1;
	}
	
	public float divide(float input_1, float input_2){
		outcome_2 = input_1 + input_2;
		return outcome_2;
	}
	
}
